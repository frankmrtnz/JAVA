
package EjerciciosHoja1.Ejercicio2;


public class Aparato {
    
    private String consumo;
    private int precio;

    public Aparato(String consumo, int precio) {
        this.consumo = consumo;
        this.precio = precio;
    }

    public String getConsumo() {
        return consumo;
    }

    public int getPrecio() {
        return precio;
    }
    
    
    
}
